function mostrar()
{
    var tabla2, mostrar;
    tabla2= document.getElementById("tabla2");
    if(tabla2.style.visibility=="hidden")
    {
        tabla2.style.visibility="visible";
        mostrar.document.getElementById("mostrar").childNodes[0];
        mostrar.data="Ocultar";
    }
    else{
        tabla2.style.visibility="hidden";
        mostrar.document.getElementById("mostrar").childNodes[0];
        mostrar.data="Mostrar";
    }
}

